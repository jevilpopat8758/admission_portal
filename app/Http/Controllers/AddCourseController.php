    {
        //dd(\Auth::check());
        $add=new Applicationdetail;
        $add->user_id=\Auth::user()->id;
        $add->applicationforprogram=$request->applicationforprogram;
        $add->coursename=$request->coursename;
        $add->applicationstatus=0;
        $add->save();
        return redirect()->route('stu.addcousrse');
    }

